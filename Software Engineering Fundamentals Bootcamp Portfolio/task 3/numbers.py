print("Please enter 3 numbers")

# get user to input 3 numbers, store each number as it's own variable
int_1 = int(input("Your first number "))
int_2 = int(input("Your second number "))
int_3 = int(input("Your third number "))

# make addition of 3 numbers a variable for use in future

addition = int_1 + int_2 + int_3

print(f"The sum of your 3 numbers is {addition}")

print(f"The first number minus the second number is {int_1 - int_2}")

print(f"The third number x first number is {int_3 * int_1}")

print(f"All 3 numbers divided by the third number is {addition / int_3}")

